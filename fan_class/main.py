print("Starting Fan Class Project")

class Fan:
    SLOW = 1
    MEDIUM = 2
    FAST = 3

    def __init__(self, speed=1, radius=5, color="blue", on=False):
        self.speed = speed
        self.radius = radius
        self.color = blue
        self.on = on

fan = Fan()

print(fan.speed)
print(fan.radius)
print(fan.color)
print(fan.on)
